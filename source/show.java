    private void show_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_show_btnActionPerformed
        try {
            stmt= con.prepareStatement("select name2,id,account_type,balance from info");
            ResultSet rs ;
            rs = stmt.executeQuery();
            while(rs.next())
            {
                name=rs.getString(1);
                id=rs.getString(2);
                accounttype=rs.getString(3);
                balance=rs.getInt(4);
                dtm.addRow(new Object []{name,id,accounttype,balance});
           tbl_show.setModel(dtm);
        } }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Feild");
        }
    }//GEN-LAST:event_show_btnActionPerformed

